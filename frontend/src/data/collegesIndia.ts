/**
 * Curated colleges for a country+state, or null when there is none (any
 * non-India country, or an India state with no curated list yet / not chosen
 * yet) — the caller should fall back to a plain free-text input in that case.
 */
export function collegesFor(country: string, state: string): string[] | null {
  if (country !== "India") return null;
  const list = COLLEGES_INDIA[state];
  return list && list.length > 0 ? list : null;
}

// Dev-only guard against the silent-failure mode described above: a key here
// that doesn't match a real `country-state-city` India state name would just
// make that state's list vanish (fall back to free-text) with no error.
if (process.env.NODE_ENV !== "production") {
  import("country-state-city").then(({ State }) => {
    const validNames = new Set(State.getStatesOfCountry("IN").map((s) => s.name));
    const badKeys = Object.keys(COLLEGES_INDIA).filter((k) => !validNames.has(k));
    if (badKeys.length > 0) {
      console.error(
        `collegesIndia.ts: these keys don't match any country-state-city India state name and will never be reachable: ${badKeys.join(", ")}`
      );
    }
  });
}
